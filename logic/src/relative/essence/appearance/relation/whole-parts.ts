/*
  Relation of Whole and Parts — A. THE RELATION OF WHOLE AND PARTS

  This module translates the Whole-Parts section:
  - Essential relation: self-subsistence reflected into itself
  - Whole: reflected unity, world existing in and for itself
  - Parts: immediate concrete existence, world of appearance
  - Reciprocal conditioning: each side conditions the other
  - Tautology: whole equals parts, parts equal whole
  - Contradiction: self-subsistence in other, not in itself
  - Transition to force-expression: immediacy passes into positedness

  PHILOSOPHICAL NOTES:

  1. **Essential Relation as Union of Realism and Idealism**:
     Essential Relation is the union of Realism (Thing/Properties/Matter) and Idealism
     (World/Law/Dharma). Whole-Parts is the first moment of Essential Relation - the
     immediate relation that contains both self-subsistence and sublatedness. Whole is
     the reflected unity (world in-and-for-itself), Parts is the immediate concrete
     existence (world of appearance). Each has the other reflectively shining in it.

  2. **The Absolute Square of Opposition: System of Marks**:
     This forms the Absolute Square of Opposition for the System of Marks - Reflective
     Validity. The structure reveals the profound analogical relations:
     
     **Foundation (Mystical/Metaphysical terms of prime import):**
     - Identity : Difference
     
     **Ground (Mystical/Metaphysical terms of prime import):**
     - Form : Matter
     
     **Essential Relation (Metaphysics terms of Art):**
     - Whole : Parts
     - Inner : Outer
     
     **Analogical Mapping:**
     - **Whole ↔ Identity**: Whole is the reflected unity, the self-subsistence reflected
       into itself. Like Identity, it is simple self-reference, abstract unity that
       stands independently. But Whole is abstract identity only when indifferent to
       parts - it needs differentiation to be truly whole.
     
     - **Parts ↔ Difference**: Parts are the immediate self-subsistence, the manifold
       immediacy. Like Difference, they are the diversified manifold, the otherness
       of unity. Parts are the unconnected manifold when indifferent to whole - they
       need unity to be truly parts.
     
     - **Inner ↔ Form**: (To be developed in Inner-Outer relation)
     
     - **Outer ↔ Matter**: (To be developed in Inner-Outer relation)
     
     This analogical structure shows Essential Relation as the realization of Foundation
     and Ground in Appearance - Identity-Difference (Foundation) becomes Whole-Parts,
     and Form-Matter (Ground) becomes Inner-Outer in the world of appearance. This is
     the Absolute Square of Opposition - the systematic mapping of mystical/metaphysical
     terms (Foundation/Ground) to metaphysics terms of Art (Essential Relation).

  3. **The Contradiction of Whole-Parts**:
     Whole is self-subsistent but has its subsistence in its opposite (parts). Whole
     consists of parts - apart from them it is nothing. Whole is totality but only
     relative - what makes it totality is its other (parts). It does not have subsistence
     within it but in its other.
     
     Parts are likewise self-subsistent but have their self-subsistence only in the
     whole. Without whole there are no parts. But because parts are self-subsistent,
     the connection is only external moment - they are indifferent to it. Yet they
     collapse together - have self-subsistence only in reflected unity (whole).
     
     Each side has self-subsistence not within but in other side. This is immediate
     contradiction - relation sublates itself.

  4. **Reciprocal Conditioning**:
     Whole and parts reciprocally condition each other. This is higher than earlier
     conditioned-condition relation - here it is realized that condition is essential
     self-subsistence of conditioned, presupposed by latter. Whole through condition
     of parts entails it is only insofar as it has parts for presupposition. Both sides
     are immediate self-subsistence but equally mediated/posited through other.
     Whole relation is turning back of conditioning into itself - non-relative,
     unconditioned.

  5. **The Tautology**:
     In essential identity: whole equals parts, parts equal whole. Nothing in whole
     not in parts, nothing in parts not in whole. Whole is unity of diversified
     manifoldness - unity within which manifold is held together is determinateness
     by which latter is parts. Relation has indivisible identity, one self-subsistence.
     
     But whole equals parts not as parts - whole is reflected unity, parts are
     otherness/diversified manifold. Whole equals them together - but "together" is
     nothing but their unity (whole). Whole as whole equals not parts but whole.
     
     Parts equal whole not as unity but as manifold - equal to apportioned whole
     (as parts). Parts as parts equal not whole but themselves (in whole).
     
     Each side refers only to itself - falls indifferently apart. But as held apart,
     they destroy themselves.

  6. **Transition to Force-Expression**:
     Truth of relation is mediation - negative unity in which both reflected and
     existent immediacy are sublated. Relation is contradiction returning to ground,
     into unity that is reflected unity but also posited as sublated, making itself
     into existent immediacy. Each side is posited as self-sublating, passing over
     into other. Relation of whole-parts passes over into relation of force-expression.

  7. **Connection to Form Processor**:
     Whole maps to Aspect (in Form Processor) - the reflected unity, the self-subsistent
     totality. Parts map to Aspect's moments - the immediate manifold. Essential Relation
     (Whole-Parts, Force-Expression, Inner-Outer) maps to Aspect as the union of Entity
     (Realism) and Property (Idealism).
*/

