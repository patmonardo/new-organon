/*
  Relation of Force and its Expression — B. THE RELATION OF FORCE AND ITS EXPRESSION

  This module translates the Force-Expression section:
  - Force: negative unity resolving contradiction of whole-parts
  - Force as activity: self-repelling contradiction
  - Conditionedness: force needs another force
  - Solicitation: reciprocal stimulation
  - Expression: externality identical with inwardness
  - Infinity: force's externality is identical with its inwardness

  PHILOSOPHICAL NOTES:

  1. **Force as Truth of Whole-Parts**:
     Force is the negative unity into which the contradiction of whole-parts has resolved
     itself - it is the truth of that first relation. Whole-parts is the "thoughtless
     relation" - a dead mechanical aggregate with external unity. Force is the higher
     immanent turning back - unity ceases to be external and indifferent to manifoldness.
     In force, immediate and reflected self-subsistence are posited as sublated moments,
     whereas in whole-parts they were self-subsisting sides or extremes.

  2. **Force as Activity**:
     Force is self-repelling contradiction - it is active. Force is self-referring negative
     unity in which reflected immediacy (essential in-itselfness) is posited as sublated
     moment, passing over into immediate concrete existence. Force becomes concretely
     existent external manifoldness from out of itself. But this transition is not only
     becoming/disappearing but negative reference to itself - force preserves itself in
     alteration. Movement is translation, not transition - force remains what it is.

  3. **Conditionedness: Force Needs Force**:
     Force is conditioned - its negative self-reference has presupposition by which it is
     solicited, and an other from which it begins. The presupposition is not a thing but
     another force. Activity of force is conditioned through itself as through an other
     to itself - through a force. Force is a relation in which each side is the same as
     the other. They are forces that stand in relation, refer to each other essentially.
     Conditionedness of force through another force is the doing of force itself in itself.

  4. **Reciprocal Solicitation**:
     Force is conditioned because moment of immediate concrete existence is posited but also
     immediate, presupposed. Externality present to force is its own activity of presupposing
     posited as another force. Presupposing is reciprocal. Each force contains unity reflected
     into itself as sublated - presupposing. It posits itself as external, but since it is
     unity reflected into itself, posits externality as another force. Force as conditioning
     is reciprocally stimulus for other force. Stimulus only solicits - force is within it
     negativity of itself, repelling from itself is its own positing. Force sublates externality
     of stimulus, posits it as its own expression.

  5. **Soliciting and Solicited**:
     Force is determined as self-sublating identity. In reality, one force is soliciting,
     other is solicited. But concept of force is identity of positing and presupposing
     reflection - each determination is moment, mediated through other. Nothing determines
     which is soliciting/solicited - both form determinations belong to each equally.
     Essential unity. One force is solicited - stimulus posited from outside. But force
     is presupposing - reflects into itself, sublates externality. That it is solicited is
     its own doing. Soliciting force refers to other negatively, sublates externality, is
     positing - but only on presupposition it has other. It is soliciting only to extent
     it is solicited. Each receives stimulus from other, but stimulus each delivers consists
     in receiving stimulus from other. Stimulus received is solicited by itself. Both given
     and received stimulus are mediated - each force is determinateness other has, mediated
     through other, which is its own determining positing.

  6. **Expression as Reaction**:
     Force expresses itself. External expression is reaction - posits externality as its own
     moment, sublates having been solicited through other force. Expression and infinite
     turning back are one. Presupposing reflection (conditionedness, stimulus) is immediately
     reflection returning into itself. Activity is essentially reactive, against itself.
     Positing of stimulus is sublation of it, and conversely, sublation of stimulus is
     positing of externality.

  7. **Infinity of Force**:
     Force is finite insofar as moments still have form of immediacy - presupposing and
     self-referring reflection are different. But activity of force consists in expressing
     itself - sublating externality, determining it as that in which it is identical with
     itself. Force expresses that its reference to other is reference to itself - passivity
     consists in activity. Stimulus by which it is solicited is its own soliciting.
     Externality is mediated by it, just as self-identity is mediated by negation.
     Force expresses that its externality is identical with its inwardness. This is the
     infinity of force - the true infinite where externality and inwardness are one.

  8. **Connection to Absolute Square of Opposition**:
     Force-Expression continues Essential Relation (Metaphysics terms of Art). Force as
     activity realizes the dynamic unity that Whole-Parts only statically represented.
     Force's infinity (externality identical with inwardness) prepares for Inner-Outer
     relation, completing the mapping: Form-Matter (Ground) → Inner-Outer (Essential Relation).
*/

